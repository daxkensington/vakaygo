import { neon } from "@neondatabase/serverless";
import { config } from "dotenv";
config({ path: ".env.local" });
const sql = neon(process.env.DATABASE_URL);
const out = {};
out.listingsByStatus = await sql`SELECT status, count(*) FROM listings GROUP BY 1 ORDER BY 2 DESC`;
out.listingsByType = await sql`SELECT type, count(*) FROM listings WHERE status='active' GROUP BY 1 ORDER BY 2 DESC`;
out.noMedia = await sql`SELECT count(*) FROM listings l WHERE l.status='active' AND NOT EXISTS (SELECT 1 FROM media m WHERE m.listing_id=l.id)`;
out.noPrimary = await sql`SELECT count(*) FROM listings l WHERE l.status='active' AND EXISTS (SELECT 1 FROM media m WHERE m.listing_id=l.id) AND NOT EXISTS (SELECT 1 FROM media m WHERE m.listing_id=l.id AND m.is_primary)`;
out.users = await sql`SELECT role, count(*) FROM users GROUP BY 1`;
out.recentUsers = await sql`SELECT count(*) FROM users WHERE created_at > now() - interval '30 days'`;
out.bookings = await sql`SELECT status, count(*) FROM bookings GROUP BY 1`;
out.recentBookings = await sql`SELECT count(*) FROM bookings WHERE created_at > now() - interval '30 days'`;
out.reviews = await sql`SELECT count(*) FROM reviews`;
out.sampleSlugs = await sql`SELECT slug, type FROM listings WHERE status='active' ORDER BY random() LIMIT 5`;
console.log(JSON.stringify(out, null, 1));
